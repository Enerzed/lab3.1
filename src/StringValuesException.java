public class StringValuesException extends Exception
{
    public StringValuesException()
    {

    }
    public StringValuesException(String message)
    {
        super(message);
    }
    public StringValuesException(String message, Throwable cause)
    {
        super(message, cause);
    }
    public StringValuesException(Throwable cause)
    {
        super(cause);
    }
    public StringValuesException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace)
    {
        super(message, cause, enableSuppression, writableStackTrace);
    }
}