public class IntegerValuesException extends Exception{
    public IntegerValuesException()
    {

    }
    public IntegerValuesException(String message)
    {
        super(message);
    }
    public IntegerValuesException(String message, Throwable cause)
    {
        super(message, cause);
    }
    public IntegerValuesException(Throwable cause)
    {
        super(cause);
    }
    public IntegerValuesException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace)
    {
        super(message, cause, enableSuppression, writableStackTrace);
    }
}
